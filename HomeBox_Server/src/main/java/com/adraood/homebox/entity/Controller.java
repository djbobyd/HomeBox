package com.adraood.homebox.entity;

import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Lob;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;
import javax.xml.bind.annotation.XmlRootElement;

/**
 *
 * @author boby
 */
@javax.persistence.Entity
@Table(name = "Controller")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "Controller.findAll", query = "SELECT c FROM Controller c"),
    @NamedQuery(name = "Controller.findById", query = "SELECT c FROM Controller c WHERE c.id = :id"),
    @NamedQuery(name = "Controller.findByName", query = "SELECT c FROM Controller c WHERE c.name = :name")})
public class Controller implements Entity {
    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "id", nullable = false)
    private Integer id;
    @Basic(optional = false)
    @Column(name = "name", nullable = false, length = 50)
    private String name;
    @Lob
    @Column(name = "description", length = 65535)
    private String description;

    public Controller() {
    }

    public Controller(Integer id) {
        this.id = id;
    }

    public Controller(Integer id, String name) {
        this.id = id;
        this.name = name;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Controller)) {
            return false;
        }
        Controller other = (Controller) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "net.dontdrinkandroot.example.angularrestspringsecurity.entity.Controller[ id=" + id + " ]";
    }
    
}
